-- DBTITLE 1,Check 10 (Data freshness check)
/*******************************************************************************
  CHECK 10 - DATA FRESHNESS / LATENCY
  One-row source vs CDM max-date comparison

  Rule:
      Compare overall max source service date to overall max CDM clinical date.

  Output:
      One row only:
      CHECK_ID | CHECK_NAME | SOURCE_MAX_DATE | CDM_MAX_DATE |
      DAYS_DIFFERENCE | STATUS | VALIDATION_NOTE

  Note:
      Gap is allowed. This query reports whether the max dates match or differ.
*******************************************************************************/

WITH SOURCE_MAX AS (

    SELECT
        MAX(SOURCE_DATE) AS SOURCE_MAX_DATE
    FROM (

        -- Pharmacy source service date
        SELECT CAST(SVCDATE AS DATE) AS SOURCE_DATE
        FROM DVMT_RWEM.RWD_MARKETSCAN_COMMERCIAL_26Q1.D_PHARM
        WHERE SVCDATE IS NOT NULL

        UNION ALL

        -- Outpatient source service date
        SELECT CAST(SVCDATE AS DATE)
        FROM DVMT_RWEM.RWD_MARKETSCAN_COMMERCIAL_26Q1.O_OUTPT
        WHERE SVCDATE IS NOT NULL

        UNION ALL

        -- Inpatient service source service date
        SELECT CAST(SVCDATE AS DATE)
        FROM DVMT_RWEM.RWD_MARKETSCAN_COMMERCIAL_26Q1.S_INPAT
        WHERE SVCDATE IS NOT NULL

        UNION ALL

        -- Inpatient summary admit date
        SELECT CAST(ADMDATE AS DATE)
        FROM DVMT_RWEM.RWD_MARKETSCAN_COMMERCIAL_26Q1.I_INPATSUM
        WHERE ADMDATE IS NOT NULL

        UNION ALL

        -- Inpatient summary discharge date
        SELECT CAST(DISDATE AS DATE)
        FROM DVMT_RWEM.RWD_MARKETSCAN_COMMERCIAL_26Q1.I_INPATSUM
        WHERE DISDATE IS NOT NULL
    )
),

CDM_MAX AS (

    SELECT
        MAX(CDM_DATE) AS CDM_MAX_DATE
    FROM (

        SELECT CAST(VISIT_START_DATE AS DATE) AS CDM_DATE
        FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.VISIT_OCCURRENCE
        WHERE VISIT_START_DATE IS NOT NULL

        UNION ALL

        SELECT CAST(VISIT_DETAIL_START_DATE AS DATE)
        FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.VISIT_DETAIL
        WHERE VISIT_DETAIL_START_DATE IS NOT NULL

        UNION ALL

        SELECT CAST(CONDITION_START_DATE AS DATE)
        FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.CONDITION_OCCURRENCE
        WHERE CONDITION_START_DATE IS NOT NULL

        UNION ALL

        SELECT CAST(DRUG_EXPOSURE_START_DATE AS DATE)
        FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.DRUG_EXPOSURE
        WHERE DRUG_EXPOSURE_START_DATE IS NOT NULL

        UNION ALL

        SELECT CAST(PROCEDURE_DATE AS DATE)
        FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.PROCEDURE_OCCURRENCE
        WHERE PROCEDURE_DATE IS NOT NULL

        UNION ALL

        SELECT CAST(DEVICE_EXPOSURE_START_DATE AS DATE)
        FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.DEVICE_EXPOSURE
        WHERE DEVICE_EXPOSURE_START_DATE IS NOT NULL

        UNION ALL

        SELECT CAST(MEASUREMENT_DATE AS DATE)
        FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.MEASUREMENT
        WHERE MEASUREMENT_DATE IS NOT NULL

        UNION ALL

        SELECT CAST(OBSERVATION_DATE AS DATE)
        FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.OBSERVATION
        WHERE OBSERVATION_DATE IS NOT NULL

        UNION ALL

        SELECT CAST(DEATH_DATE AS DATE)
        FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.DEATH
        WHERE DEATH_DATE IS NOT NULL

        UNION ALL

        SELECT CAST(DRUG_ERA_END_DATE AS DATE)
        FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.DRUG_ERA
        WHERE DRUG_ERA_END_DATE IS NOT NULL

        UNION ALL

        SELECT CAST(DOSE_ERA_END_DATE AS DATE)
        FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.DOSE_ERA
        WHERE DOSE_ERA_END_DATE IS NOT NULL

        UNION ALL

        SELECT CAST(CONDITION_ERA_END_DATE AS DATE)
        FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.CONDITION_ERA
        WHERE CONDITION_ERA_END_DATE IS NOT NULL
    )
)

SELECT
    '10' AS CHECK_ID,
    'DATA FRESHNESS / LATENCY - SOURCE MAX DATE VS CDM MAX DATE' AS CHECK_NAME,
    S.SOURCE_MAX_DATE,
    C.CDM_MAX_DATE,
    DATEDIFF(S.SOURCE_MAX_DATE, C.CDM_MAX_DATE) AS DAYS_DIFFERENCE,
    CASE
        WHEN S.SOURCE_MAX_DATE IS NULL AND C.CDM_MAX_DATE IS NULL THEN 'NO DATA'
        WHEN S.SOURCE_MAX_DATE = C.CDM_MAX_DATE THEN 'MATCH'
        ELSE 'DATE_GAP_REPORTED'
    END AS STATUS,
    CASE
        WHEN S.SOURCE_MAX_DATE IS NULL AND C.CDM_MAX_DATE IS NULL
            THEN 'No populated source or CDM dates found'
        WHEN S.SOURCE_MAX_DATE = C.CDM_MAX_DATE
            THEN 'Source and CDM max dates match'
        WHEN C.CDM_MAX_DATE < S.SOURCE_MAX_DATE
            THEN 'CDM max date is earlier than source max date; gap reported for latency review'
        WHEN C.CDM_MAX_DATE > S.SOURCE_MAX_DATE
            THEN 'CDM max date is later than source max date; gap reported for derivation/source-filter review'
        ELSE 'Date comparison completed'
    END AS VALIDATION_NOTE,
    'DATA FRESHNESS' AS CHECK_TYPE
FROM SOURCE_MAX S
CROSS JOIN CDM_MAX C;

