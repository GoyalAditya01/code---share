-- DBTITLE 1,Check 3 (patient coverge)
/*******************************************************************************
  CHECK 03 - SOURCE vs OMOP PATIENT COVERAGE
  Scope  : T_ENROLL source patients vs PERSON CDM patients
  Fix    : Excludes NULL ENROLID because NULL is not a valid source patient ID.
  PASS   : < 0.1 percent valid enrolled patients missing from PERSON
*******************************************************************************/

WITH
ENROLL_PATIENTS AS (
    SELECT DISTINCT
        CAST(ENROLID AS STRING) AS SOURCE_PATIENT_ID
    FROM DVMT_RWEM.RWD_MARKETSCAN_COMMERCIAL_26Q1.T_ENROLL
    WHERE ENROLID IS NOT NULL
),

CDM_PATIENTS AS (
    SELECT DISTINCT
        CAST(PERSON_SOURCE_VALUE AS STRING) AS PERSON_SOURCE_VALUE
    FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.PERSON
    WHERE PERSON_SOURCE_VALUE IS NOT NULL
),

MISSING AS (
    SELECT
        E.SOURCE_PATIENT_ID
    FROM ENROLL_PATIENTS E
    LEFT JOIN CDM_PATIENTS C
        ON E.SOURCE_PATIENT_ID = C.PERSON_SOURCE_VALUE
    WHERE C.PERSON_SOURCE_VALUE IS NULL
),

SUMMARY AS (
    SELECT
        (SELECT COUNT(*) FROM ENROLL_PATIENTS) AS T_ENROLL_VALID_PATIENT_COUNT,
        (SELECT COUNT(*) FROM CDM_PATIENTS) AS PERSON_TABLE_COUNT,
        (SELECT COUNT(*) FROM MISSING) AS MISSING_FROM_CDM
)

SELECT
    T_ENROLL_VALID_PATIENT_COUNT,
    PERSON_TABLE_COUNT,
    MISSING_FROM_CDM,
    ROUND(
        MISSING_FROM_CDM * 100.0 / T_ENROLL_VALID_PATIENT_COUNT,
        8
    ) AS MISSING_PERCENT,
    CASE
        WHEN MISSING_FROM_CDM * 100.0 / T_ENROLL_VALID_PATIENT_COUNT < 0.1
            THEN 'PASS'
        ELSE 'FAIL'
    END AS STATUS,
    'NULL ENROLID values excluded because they are not valid source patient identifiers' AS VALIDATION_NOTE,
     'SOURCE VS OMOP PATIENT COVERAGE' AS CHECK_TYPE
FROM SUMMARY;

