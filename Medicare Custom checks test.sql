-- DBTITLE 1,Check 9 (Concept mapping  accuracy)
/******************************************************************************
CHECK 09 - CONCEPT MAPPING ACCURACY (TOP-N)

Purpose:
Validate top frequent source values are correctly mapped to standard concepts.

Includes:
- OMOP + Oncology Extension tables
- VISIT_OCCURRENCE (hardcoded domain mapping)
- PROVIDER (specialty normalization fix)

Logic:
1. Extract Top N source values
2. Normalize where required
3. Join SOURCE_TO_STANDARD_MAP
4. Validate against CONCEPT table
******************************************************************************/

WITH PARAMS AS (
    SELECT 100 AS TOP_N
),

/* =========================
   SOURCE VALUES
   ========================= */
SOURCE_VALUES AS (

    SELECT 'CONDITION_OCCURRENCE' AS TABLE_NAME,
           CONDITION_SOURCE_VALUE AS SOURCE_VALUE
    FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.CONDITION_OCCURRENCE
    WHERE CONDITION_SOURCE_VALUE IS NOT NULL

    UNION ALL
    SELECT 'PROCEDURE_OCCURRENCE', PROCEDURE_SOURCE_VALUE
    FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.PROCEDURE_OCCURRENCE
    WHERE PROCEDURE_SOURCE_VALUE IS NOT NULL

    UNION ALL
    SELECT 'DRUG_EXPOSURE', DRUG_SOURCE_VALUE
    FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.DRUG_EXPOSURE
    WHERE DRUG_SOURCE_VALUE IS NOT NULL

    UNION ALL
    SELECT 'MEASUREMENT', MEASUREMENT_SOURCE_VALUE
    FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.MEASUREMENT
    WHERE MEASUREMENT_SOURCE_VALUE IS NOT NULL

    UNION ALL
    SELECT 'OBSERVATION', OBSERVATION_SOURCE_VALUE
    FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.OBSERVATION
    WHERE OBSERVATION_SOURCE_VALUE IS NOT NULL

    UNION ALL
    SELECT 'DEVICE_EXPOSURE', DEVICE_SOURCE_VALUE
    FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.DEVICE_EXPOSURE
    WHERE DEVICE_SOURCE_VALUE IS NOT NULL

    UNION ALL
    SELECT 'DEATH', CAUSE_SOURCE_VALUE
    FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.DEATH
    WHERE CAUSE_SOURCE_VALUE IS NOT NULL

    UNION ALL
    SELECT 'PROVIDER',
           UPPER(TRIM(SPLIT_PART(SPECIALTY_SOURCE_VALUE, '-', 2))) AS SOURCE_VALUE
    FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.PROVIDER
    WHERE SPECIALTY_SOURCE_VALUE IS NOT NULL

    UNION ALL
    SELECT 'TUMOR_EPISODE', EPISODE_SOURCE_VALUE
    FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.TUMOR_EPISODE
    WHERE EPISODE_SOURCE_VALUE IS NOT NULL

    UNION ALL
    SELECT 'GENOMIC_MEASUREMENT', MEASUREMENT_SOURCE_VALUE
    FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.GENOMIC_MEASUREMENT
    WHERE MEASUREMENT_SOURCE_VALUE IS NOT NULL

    UNION ALL
    SELECT 'BIOMARKER_DETAIL', BIOMARKER_DETAIL_SOURCE_VALUE
    FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.BIOMARKER_DETAIL
    WHERE BIOMARKER_DETAIL_SOURCE_VALUE IS NOT NULL
),

/* =========================
   TOP N SOURCE VALUES
   ========================= */
TOP_SOURCE_VALUES AS (
    SELECT *
    FROM (
        SELECT
            TABLE_NAME,
            SOURCE_VALUE,
            COUNT(*) AS FREQUENCY,
            ROW_NUMBER() OVER (
                PARTITION BY TABLE_NAME
                ORDER BY COUNT(*) DESC
            ) AS RN
        FROM SOURCE_VALUES
        GROUP BY TABLE_NAME, SOURCE_VALUE
    ) X
    CROSS JOIN PARAMS P
    WHERE RN <= P.TOP_N
),

/* =========================
   SOURCE TO STANDARD MAP
   ========================= */
MAP AS (
    SELECT
        UPPER(TRIM(SOURCE_CODE)) AS SOURCE_CODE,
        SOURCE_CONCEPT_NAME,
        TARGET_CONCEPT_ID,
        TARGET_CONCEPT_NAME,
        TARGET_DOMAIN_ID,
        TARGET_STANDARD_CONCEPT,
        TARGET_INVALID_REASON
    FROM DVMT_RWPL.MARKETSCAN_COMMERCIAL_CDM_2026Q1_INT.SOURCE_TO_STANDARD_MAP
),

/* =========================
   CONCEPT TABLE
   ========================= */
CONCEPT AS (
    SELECT
        CONCEPT_ID,
        STANDARD_CONCEPT,
        INVALID_REASON
    FROM DVMT_RWPL.ATHENA_CONCEPT_MAPPING.CONCEPT
),

/* =========================
   FINAL EVALUATION
   ========================= */
EVAL AS (
    SELECT
        T.TABLE_NAME,
        T.SOURCE_VALUE,
        T.FREQUENCY,

        M.TARGET_CONCEPT_ID,
        M.TARGET_CONCEPT_NAME,
        M.TARGET_DOMAIN_ID,
        M.TARGET_STANDARD_CONCEPT,

        C.STANDARD_CONCEPT AS CONCEPT_STANDARD_CONCEPT,
        C.INVALID_REASON AS CONCEPT_INVALID_REASON,

        CASE
            WHEN M.TARGET_CONCEPT_ID IS NULL THEN 'NOT MAPPED'
            WHEN C.CONCEPT_ID IS NULL THEN 'INVALID CONCEPT'
            WHEN C.INVALID_REASON IS NOT NULL THEN 'INVALID CONCEPT'
            WHEN UPPER(C.STANDARD_CONCEPT) <> 'S' THEN 'NOT STANDARD'
            ELSE 'PASS'
        END AS MAPPING_STATUS

    FROM TOP_SOURCE_VALUES T

    LEFT JOIN MAP M
        ON  UPPER(TRIM(T.SOURCE_VALUE)) = UPPER(TRIM(M.SOURCE_CODE))
        OR  UPPER(TRIM(T.SOURCE_VALUE)) = UPPER(TRIM(M.SOURCE_CONCEPT_NAME))

    LEFT JOIN CONCEPT C
        ON M.TARGET_CONCEPT_ID = C.CONCEPT_ID
)

/* =========================
   FINAL OUTPUT
   ========================= */
SELECT
    TABLE_NAME,
    SOURCE_VALUE,
    FREQUENCY,
    TARGET_CONCEPT_ID,
    TARGET_CONCEPT_NAME,
    TARGET_DOMAIN_ID,
    CONCEPT_STANDARD_CONCEPT,
    CONCEPT_INVALID_REASON,
    MAPPING_STATUS
FROM EVAL
ORDER BY TABLE_NAME, FREQUENCY DESC;

