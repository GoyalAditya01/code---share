-- DBTITLE 1,Check 11 (Source patient id collision)
/*******************************************************************************
  CHECK 11 - PATIENT ID COLLISION
  Scope : Verify PERSON_SOURCE_VALUE → PERSON_ID is 1:1 mapping
  Result: TABLE_NAME | COLUMN_NAME | RULE | STATUS
