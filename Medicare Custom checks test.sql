      AND a.DATA_TYPE IN ('BIGINT','LONG') THEN 'PASS'
    -- int: accept INT, INTEGER, SMALLINT, TINYINT
    WHEN UPPER(e.EXPECTED_TYPE) = 'INT'
      AND a.DATA_TYPE IN ('INT','INTEGER','SMALLINT','TINYINT') THEN 'PASS'
    -- float: accept FLOAT, DOUBLE, DECIMAL
    WHEN UPPER(e.EXPECTED_TYPE) = 'FLOAT'
      AND a.DATA_TYPE IN ('FLOAT','DOUBLE','DECIMAL') THEN 'PASS'
    -- string: accept STRING, VARCHAR, CHAR, TEXT
    WHEN UPPER(e.EXPECTED_TYPE) = 'STRING'
      AND a.DATA_TYPE IN ('STRING','VARCHAR','CHAR','TEXT') THEN 'PASS'
    -- date
    WHEN UPPER(e.EXPECTED_TYPE) = 'DATE'
      AND a.DATA_TYPE = 'DATE' THEN 'PASS'
    -- timestamp
    WHEN UPPER(e.EXPECTED_TYPE) = 'TIMESTAMP'
      AND a.DATA_TYPE IN ('TIMESTAMP','TIMESTAMP_NTZ','TIMESTAMP_LTZ') THEN 'PASS'
    -- boolean
    WHEN UPPER(e.EXPECTED_TYPE) = 'BOOLEAN'
      AND a.DATA_TYPE IN ('BOOLEAN','BOOL') THEN 'PASS'
    -- column not found in actual schema
    WHEN a.DATA_TYPE IS NULL THEN 'FAIL'
    ELSE 'FAIL'
